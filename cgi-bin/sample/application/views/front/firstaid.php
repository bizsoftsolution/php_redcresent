							Why learn First Aid? <br>
							First aid is the immediate care given to a person who is injured or suddenly becomes ill. In an emergency situation lack of first aid knowledge could lead to injuries and disabilities and even unnecessary deaths. As such it is important that individuals are equipped with basic first aid and CPR as it could make a difference between life and death.<br>
