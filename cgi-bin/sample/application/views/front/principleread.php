		  <strong>Humanity  </strong><br><br>
         			    
Born of a desire to bring assistance without discrimination to the wounded, we endeavour to prevent and alleviate human suffering wherever it may be found. The aim is to protect life and health and ensure respect for the human being. This promotes mutual understanding, friendship, co-operation and lasting peace amongst all peoples.<br><br>

           <strong>  Impartiality </strong><br><br>
		   We make no discrimination as to nationality, race, religious beliefs, class or political opinions. We endeavour to relief the suffering of individuals, being guided solely by their needs, and to give priority to the most urgent cases of distress.<br><br>
		   <strong> Neutrality </strong><br><br>
		   In order to continue to enjoy the confidence of all, we shall not take sides in hostilities or engage at any time in controversies of a political, racial, religious or ideological nature.<br><br>
		   <strong> Independence </strong><br><br>
		   We are independent. And while we are auxiliary in the humanitarian services of the Malaysian government and subject to the laws of the country, we maintain autonomy so that we may be able at all times to act in accordance with the principles of the movement.<br><br>
		   <strong> Voluntary Service </strong><br><br>
		   We are a voluntary relief movement not prompted in any manner by desire for gain.<br><br>
		   <strong>Unity  </strong><br><br>
		   There can be only one Red Cross or one Red Crescent Society in any one country. It must be open to all. It must carry on its humanitarian work throughout its territory.<br><br>
		   <strong>Universality  </strong><br><br>
