                   <P style="text-align:justify;">
				   The Malaysian Red Crescent (MRC) has its beginnings in 1948 as branches of the British Red Cross Society in Sabah and Sarawak (then British North Borneo). In the 1950s the British Red Cross Society further established branches in the other parts of Malaysia starting in Penang in 1950, and later in the other states.<br><br>
				   Upon Malaya's independence in 1957, the branches in Peninsular Malaysia were organised as the Federation of Malaya Red Cross Society and were later statutorily incorporated by the Federation of Malaya Red Cross Society (Incorporation) Act, 1962. MRCS received official recognition as an independent National Society from the International Committee of the Red Cross (ICRC) on 4th July 1963 and accordingly was admitted as a member of the International League of Red Cross and Red Crescent Societies on 24th August 1963.<br><br>
				   
				   The Movement is Madeup of nearly 100million members,volunteers and supporters in 189 National Societies. it has three main Components:<br><br>
				   1) The International Commitee of the Red Cross (ICRC).<br>
                   2) The International Federation of Red Cross and Red Crescent Society (IFRC).<br>
                   3) 189 Member Red Cross and Red Crescent Societies.<br> <br>
