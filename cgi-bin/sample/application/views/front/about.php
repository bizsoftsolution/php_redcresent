                            <p>
            The Malaysian Red Crescent (MRC) is a non-profit organisation dedicated to humanitarian acts and services. Voluntary in nature, it is part of the International Red Cross and Red Crescent Movement, the world's largest independent humanitarian network. The Movement, as it is widely known as, is largely perceived as a “public good” – available to everyone, everywhere, to prevent and reduce human sufferings. <br><br>

Presently one of 186 National Societies worldwide members of the International Federation of Red Cross and Red Crescent Societies, MRC runs programmes and activities in accordance with the spirit and requirements of its Incorporation Act of 1965 and the Movement's fundamental principles. <br><br>

Our emblem – the Red Crescent – flies with pride as a symbol of trust and integrity and the ideals of the Movement are represented in Malaysia not just through the visible impact of our work but also by influencing hearts and minds of individuals to be more caring and compassionate.  <br><br>
 
Whilst is plays a crucial role in environments of danger where human safety is at risk, such as war and natural disasters it serves the community at peace time through first aid services and humanitarian programmes for the needy and helpless. <br><br>

The Malaysian Red Crescent is proud to have helped Malaysians through disasters such as the riots of 1969, the Northern Region floods of 2010, the 2004 Tsunami that hit Acheh and subsequently Penang and more recently the insurgence in Lahad Datu. <br><br>

MRC has also played a role in helping victims in other nations such as Palestine, Japan, Pakistan, Burma and China through raising relief funds and disaster recovery and first aid services. <br><br>

Today the Malaysian Red Crescent comprises approximately 300,000 volunteers serving 13 branches in every state and the National Headquarters based in Kuala Lumpur. Amongst this are our youth volunteers from schools and institutions of higher learning throughout the nation.<br><br>

On a global level, the Red Cross and Red Crescent Movement celebrate its 150th Anniversary this year with the theme: “150 Years of Humanitarian Action”. <br><br>

Simultaneously the Malaysian Red Crescent celebrates 65 years in Malaysia.
